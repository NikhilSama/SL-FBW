			{
				$photo['created'] = $photo_data['created_time'];
			}

			if( !empty($photo_data['source']) ) 
			{
				$photo['largeImg'] = $photo_data['source'];
			} 

			if( !empty($photo_data['name']) ) 
			{
				$photo['caption'] = $photo_data['name'];
			}
